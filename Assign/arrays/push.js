// add new items in the end of an array

const fruits = ["Apple", "Grapes", "Orange", "Banana"];

console.log(fruits); // [ 'Apple', 'Grapes', 'Orange', 'Banana' ]
fruits.push("Mangoes");

console.log(fruits); // [ 'Apple', 'Grapes', 'Orange', 'Banana', 'Mangoes' ]
console.log(fruits.length); //5
console.log(fruits.length-1); //4